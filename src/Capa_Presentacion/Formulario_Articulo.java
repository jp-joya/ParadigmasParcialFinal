    public void actualizarFactura() {
		int filas = carritoModelo.getRowCount();
		for(int i=0;filas>i;i++) {
			carritoModelo.removeRow(0);
		}
			
		for (DataOrden orden:pedido.getDataOrdenes()) {
			Object[] fila =new Object[3];
			fila[0] = orden.getProducto().getArt_nom();
			fila[1] = orden.getProducto().getArt_pre();
			fila[2] = orden.getCantidad();
		
			carritoModelo.addRow(fila);
		}		
    }
