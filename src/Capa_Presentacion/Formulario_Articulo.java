    public void ListarPedidos(){
        
        DefaultTableModel tabla = new DefaultTableModel();
        DataPedido objPed = new DataPedido();
        ArrayList<DataPedido> lista = new ArrayList();
        lista = objPed.ListaPedidos();
        tabla.addColumn("Fecha");
        tabla.addColumn("Qty");
        tabla.addColumn("Total");
        tabla.setRowCount(lista.size());
        int i = 0;
        for (DataPedido x : lista) {
            tabla.setValueAt(x.getFecha(), i, 0);
            tabla.setValueAt(x.getCantidadProductos(),i, 1);
            tabla.setValueAt(x.getTotal(), i, 2);
            i++;
        }
        this.jTable3.setModel(tabla);
    }
    public void ListarArticulos(){
        
        DefaultTableModel tabla = new DefaultTableModel();
        DataArticulo objart = new DataArticulo();
        ArrayList<DataArticulo> lista2 = new ArrayList();
        lista2= objart.ListaArticulos();
        tabla.addColumn("Codigo");
        tabla.addColumn("Nombre");
        tabla.addColumn("Precio");
        tabla.setRowCount(lista2.size());
        int i = 0;
        for (DataArticulo x : lista2) {
            tabla.setValueAt(x.getArt_cod(), i, 0);
            tabla.setValueAt(x.getArt_nom(),i, 1);
            tabla.setValueAt(x.getArt_pre(), i, 2);
            i++;
        }
        this.jTable1.setModel(tabla);
    }
